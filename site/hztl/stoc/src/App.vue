<template>
  <div id="app">
    <!-- <keep-alive> -->
      <router-view v-if="isRouterAlive" />
    <!-- </keep-alive> -->
  </div>
</template>

<script>
export default {
  name: "app",
  provide() {
    return {
      reload: this.reload
    };
  },
  data() {
    return {
      isRouterAlive: true
    };
  },
  created() {
    this.$store.commit("city/setActiveAreaCity");
  },
  methods: {
    reload() {
      this.isRouterAlive = false;
      this.$nextTick(() => {
        this.isRouterAlive = true;
      });
    }
  }
};
</script>
