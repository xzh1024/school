class Greeter {
    greeting: string;
    constructor(message: string) {
        this.greeting = message;
    }
    greet() {
        return "Hello, " + this.greeting;
    }
}

let greeter = new Greeter("world");

class GreeterChildren extends Greeter {
    constructor(greeting){
        super(greeting);
    }
}
let greeter2 = new GreeterChildren("hello");

